package day17Loops_for;

public class C07_NestedForLoop {

	public static void main(String[] args) {
		// 1 de 4 kadar sayilari yan yana aralarinda bosluk birakarak yazdirion
  for (int satir = 1; satir <=3; satir++) {

	for (int i = 1; i <=3; i++) {
		
		System.out.print(satir*i + " ");
	}
	System.out.println("");
	
  
  }
  
  
  
  
      
	}
}
